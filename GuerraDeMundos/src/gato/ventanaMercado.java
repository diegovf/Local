    private void botonMercadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonMercadosMouseClicked
        int dineroComparacion = Integer.parseInt(dineroLabel.getText());
        if(dineroComparacion >= 2000)
        {
        JuegoGato.Mercados++;
        JuegoGato.dinero -= 2000;
        setLabelDinero(JuegoGato.dinero);
        JOptionPane.showMessageDialog(null,"Compra de mercado realizada con √©xito","Error",1);
        }
        else
            JOptionPane.showMessageDialog(null,"No tienes suficiente dinero","Error",0);
    }//GEN-LAST:event_botonMercadosMouseClicked

